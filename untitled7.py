    ("ข", "ขอ ไข่ (khor khai)"),
    ("ค", "คอ ควาย (khor khwai)"),
    ("ง", "งอ งู (ngor ngu)"),
    ("จ", "จอ จาน (jor jaan)"),
    ("ฉ", "ฉอ ฉิ่ง (chor ching)"),
    ("ช", "ชอ ช้าง (chor chaang)"),
    ("ซ", "ซอ โซ่ (sor so)"),
    ("ญ", "ญอ หญิง (yor ying)"),
    ("ฎ", "ฎอ ชฎา (dor cha-daa)"),
    ("ฐ", "ฐอ ฐาน (thor thaan)"),
    ("ณ", "ณอ เณร (nor nen)"),
    ("ด", "ดอ เด็ก (dor dek)"),
