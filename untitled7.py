    ("ท", "ทอ ทหาร (thor thahaan)"),
    ("น", "นอ หนู (nor nu)"),
    ("บ", "บอ ใบไม้ (bor bai-mai)"),
    ("ป", "ปอ ปลา (por plaa)"),
